package anujitha;

public class Admin {
    
    private String userName;
    private String passWord;
    
    public Admin(String userName, String passWord) {
        super();
        this.userName = userName;
        this.passWord = passWord;
    }

 

    public String getUserName() {
        return userName;
    }

 

    public String getPassWord() {
        return passWord;
    }
    
}
 